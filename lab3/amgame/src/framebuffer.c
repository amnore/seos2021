#include "framebuffer.h"
#include "am.h"
#include "amdev.h"
#include "kalloc.h"
#include "klib-macros.h"
#include <stdbool.h>
#include <stdint.h>

#define FONT_WIDTH 8
#define FONT_HEIGHT 16
#define FONT_NCHARS 256

typedef uint32_t Pixels[FONT_WIDTH * FONT_HEIGHT];

struct FrameBufferTexture {
  Pixels textures[FONT_NCHARS];
};

typedef struct FrameBufferCell {
  Pixels *pixels;
  bool dirty;
} FrameBufferCell;

static uint8_t fb_term_font[FONT_WIDTH * FONT_HEIGHT * FONT_NCHARS / 8];

static int fb_width, fb_height;

static FrameBufferCell *fb;

static Pixels pixels_black;

static void fb_texture_fill(Pixels pixels, uint8_t *bits, uint32_t fg,
                            uint32_t bg) {
  uint32_t *px = pixels;
  for (int y = 0; y < FONT_HEIGHT; y++)
    for (int x = 0; x < FONT_WIDTH; x++) {
      *px++ = ((bits[y] >> (7 - x)) & 1) ? fg : bg;
    }
}

void framebuffer_init() {
  AM_GPU_CONFIG_T cfg = io_read(AM_GPU_CONFIG);
  fb_width = cfg.width / FONT_WIDTH;
  fb_height = cfg.height / FONT_HEIGHT;
  fb = kalloc(fb_width * fb_height * sizeof(*fb));
  for (int i = 0; i < fb_width * fb_height; i++) {
    fb[i] = (struct FrameBufferCell){.dirty = true, .pixels = NULL};
  }
}

FrameBufferTexture *framebuffer_load_font(uint32_t fg, uint32_t bg) {
  FrameBufferTexture *font = kalloc(sizeof(FrameBufferTexture));
  for (int ch = 0; ch < FONT_NCHARS; ch++) {
    fb_texture_fill(font->textures[ch], &fb_term_font[16 * ch], fg, bg);
  }

  return font;
}

int framebuffer_get_width() { return fb_width; }

int framebuffer_get_height() { return fb_height; }

void framebuffer_putch(int w, int h, char ch, FrameBufferTexture *font) {
  FrameBufferCell *cell = &fb[h * fb_width + w];
  Pixels *pixels = &font->textures[(int)ch];
  *cell = (FrameBufferCell){.pixels = pixels, .dirty = cell->pixels != pixels};
}

void framebuffer_repaint() {
  for (int i = 0; i < fb_height; i++) {
    for (int j = 0; j < fb_width; j++) {
      FrameBufferCell cell = fb[i * fb_width + j];
      if (!cell.dirty) {
        continue;
      }

      AM_GPU_FBDRAW_T ev = {.x = j * FONT_WIDTH,
                            .y = i * FONT_HEIGHT,
                            .w = FONT_WIDTH,
                            .h = FONT_HEIGHT,
                            .sync = true,
                            .pixels =
                                cell.pixels ? *cell.pixels : pixels_black};
      ioe_write(AM_GPU_FBDRAW, &ev);
    }
  }
}

